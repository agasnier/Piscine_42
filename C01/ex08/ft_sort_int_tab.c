void	ft_sort_int_tab(int *tab, int size)
{
	int	n;
	int	tmp;

	n = 0;
	while (n < size)
	{
		if ( n > 0 && tab[n] < tab[n - 1])
		{
			tmp = tab[n];
			tab[n] = tab[n - 1];
			tab[n - 1] = tmp;
			n--;
		}
		else
		{
			n++;
		}
	}
}
